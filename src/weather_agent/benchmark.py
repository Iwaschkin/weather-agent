"""Opt-in benchmark of the agent's cost and tool routing over a query set.

Runs each query through a fresh agent (so per-run token and latency metrics are
isolated, since Strands accumulates them across an agent's lifetime) with a
:class:`~weather_agent.observability.ToolCallObserver`, then aggregates tokens,
model latency, and tool-call distribution across the runs. Aggregation and
formatting are pure and tested; running the queries performs network I/O against
an Ollama server and is therefore offline / opt-in, like
:mod:`weather_agent.eval_llm`.
"""

from __future__ import annotations

import math
from dataclasses import dataclass
from datetime import UTC, datetime
from typing import TYPE_CHECKING

from weather_agent.agent import DEFAULT_MODEL_ID, DEFAULT_OLLAMA_HOST, build_agent
from weather_agent.observability import ToolCallObserver

if TYPE_CHECKING:
    from collections.abc import Callable, Sequence

    from weather_agent.observability import ToolCall

_DEFAULT_QUERIES: tuple[str, ...] = (
    "What is the current weather in Tokyo?",
    "What will the weather be in Berlin tomorrow?",
    "Which is warmest: Paris, London, or Berlin?",
    "What is the air quality in Beijing?",
    "Can I fly my DJI Neo in Congleton tomorrow?",
)


@dataclass(frozen=True, slots=True)
class RunStat:
    """Cost and tool-routing record for one benchmarked query.

    Attributes:
        query: The prompt that was run.
        tool_calls: The tool calls the model made answering it.
        input_tokens: Prompt tokens sent to the model for this run.
        output_tokens: Tokens generated by the model for this run.
        total_tokens: Combined token count for this run.
        model_latency_ms: Model (inference) latency for this run, in milliseconds.
    """

    query: str
    tool_calls: tuple[ToolCall, ...]
    input_tokens: int
    output_tokens: int
    total_tokens: int
    model_latency_ms: int


@dataclass(frozen=True, slots=True)
class BenchmarkSummary:
    """Aggregate cost and tool-routing across a benchmark run.

    Attributes:
        runs: Number of queries benchmarked.
        total_tokens: Total tokens across all runs.
        mean_total_tokens: Mean total tokens per run.
        mean_input_tokens: Mean prompt tokens per run.
        mean_output_tokens: Mean generated tokens per run.
        mean_latency_ms: Mean model latency per run, in milliseconds.
        p50_latency_ms: Median model latency, in milliseconds.
        p95_latency_ms: 95th-percentile model latency, in milliseconds.
        total_tool_calls: Total tool calls across all runs.
        failed_tool_calls: Tool calls that returned an error.
        tool_counts: Per-tool call counts, ordered by count then name.
    """

    runs: int
    total_tokens: int
    mean_total_tokens: float
    mean_input_tokens: float
    mean_output_tokens: float
    mean_latency_ms: float
    p50_latency_ms: float
    p95_latency_ms: float
    total_tool_calls: int
    failed_tool_calls: int
    tool_counts: tuple[tuple[str, int], ...]


@dataclass(frozen=True, slots=True)
class BenchmarkReport:
    """A benchmark run with the metadata needed to archive or compare it.

    The summary is derived from ``runs`` rather than stored, so a report has a
    single source of truth and cannot hold a summary inconsistent with its runs.

    Attributes:
        model_id: The Ollama model tag benchmarked.
        host: The Ollama server the run targeted.
        captured_at: Timezone-aware UTC time the run was captured.
        runs: The per-query records.
    """

    model_id: str
    host: str
    captured_at: datetime
    runs: tuple[RunStat, ...]

    @property
    def summary(self) -> BenchmarkSummary:
        """The aggregate summary computed from this report's runs."""
        return aggregate_runs(self.runs)


def _utc_now() -> datetime:
    """Return the current timezone-aware UTC time (the default capture clock)."""
    return datetime.now(UTC)


def _percentile(values: Sequence[float], pct: float) -> float:
    """Return the nearest-rank percentile of ``values``.

    Args:
        values: The sample values; may be empty.
        pct: The percentile to compute, from 0 to 100.

    Returns:
        The nearest-rank percentile, or 0.0 when ``values`` is empty.
    """
    ordered = sorted(values)
    if not ordered:
        return 0.0
    rank = max(1, math.ceil(pct / 100 * len(ordered)))
    return ordered[rank - 1]


def aggregate_runs(runs: Sequence[RunStat]) -> BenchmarkSummary:
    """Aggregate per-run records into a single benchmark summary.

    Args:
        runs: The per-query records to aggregate.

    Returns:
        The aggregate summary. An empty input yields an all-zero summary.
    """
    if not runs:
        return BenchmarkSummary(
            runs=0,
            total_tokens=0,
            mean_total_tokens=0.0,
            mean_input_tokens=0.0,
            mean_output_tokens=0.0,
            mean_latency_ms=0.0,
            p50_latency_ms=0.0,
            p95_latency_ms=0.0,
            total_tool_calls=0,
            failed_tool_calls=0,
            tool_counts=(),
        )
    n = len(runs)
    latencies = [float(run.model_latency_ms) for run in runs]
    counts: dict[str, int] = {}
    total_calls = 0
    failed = 0
    for run in runs:
        for call in run.tool_calls:
            total_calls += 1
            counts[call.name] = counts.get(call.name, 0) + 1
            if not call.succeeded:
                failed += 1
    return BenchmarkSummary(
        runs=n,
        total_tokens=sum(run.total_tokens for run in runs),
        mean_total_tokens=sum(run.total_tokens for run in runs) / n,
        mean_input_tokens=sum(run.input_tokens for run in runs) / n,
        mean_output_tokens=sum(run.output_tokens for run in runs) / n,
        mean_latency_ms=sum(latencies) / n,
        p50_latency_ms=_percentile(latencies, 50),
        p95_latency_ms=_percentile(latencies, 95),
        total_tool_calls=total_calls,
        failed_tool_calls=failed,
        tool_counts=tuple(sorted(counts.items(), key=lambda item: (-item[1], item[0]))),
    )


def format_summary(summary: BenchmarkSummary) -> str:
    """Render a benchmark summary as a readable multi-line report.

    Args:
        summary: The aggregate summary to render.

    Returns:
        A multi-line report, or ``"no runs"`` when the summary is empty.
    """
    if summary.runs == 0:
        return "no runs"
    lines = [
        f"runs: {summary.runs}",
        f"tokens: {summary.total_tokens} total, {summary.mean_total_tokens:.0f} mean/run "
        f"(in {summary.mean_input_tokens:.0f}, out {summary.mean_output_tokens:.0f})",
        f"model latency: {summary.mean_latency_ms:.0f} ms mean, "
        f"{summary.p50_latency_ms:.0f} ms p50, {summary.p95_latency_ms:.0f} ms p95",
        f"tool calls: {summary.total_tool_calls} total, {summary.failed_tool_calls} failed",
    ]
    if summary.tool_counts:
        breakdown = ", ".join(f"{name} x{count}" for name, count in summary.tool_counts)
        lines.append(f"tool breakdown: {breakdown}")
    return "\n".join(lines)


def run_benchmark(
    queries: Sequence[str] = _DEFAULT_QUERIES,
    *,
    host: str = DEFAULT_OLLAMA_HOST,
    model_id: str = DEFAULT_MODEL_ID,
    clock: Callable[[], datetime] = _utc_now,
) -> BenchmarkReport:
    """Run each query through a fresh agent and capture a benchmark report.

    Performs network I/O against an Ollama server (a running server with
    ``model_id`` pulled is required), so this is for offline / opt-in
    benchmarking only. A fresh agent is built per query so each run's token and
    latency metrics are isolated rather than accumulated.

    Args:
        queries: The prompts to benchmark.
        host: Base URL of the Ollama server.
        model_id: Ollama model tag to benchmark.
        clock: Capture-time clock; injectable so the timestamp is controllable.

    Returns:
        A :class:`BenchmarkReport` carrying one :class:`RunStat` per query (in
        input order) plus the run metadata.
    """
    captured_at = clock()
    stats: list[RunStat] = []
    for query in queries:
        observer = ToolCallObserver()
        agent = build_agent(host=host, model_id=model_id, observer=observer)
        result = agent(query)
        usage = result.metrics.accumulated_usage
        stats.append(
            RunStat(
                query=query,
                tool_calls=observer.calls,
                input_tokens=usage["inputTokens"],
                output_tokens=usage["outputTokens"],
                total_tokens=usage["totalTokens"],
                model_latency_ms=result.metrics.accumulated_metrics["latencyMs"],
            )
        )
    return BenchmarkReport(model_id=model_id, host=host, captured_at=captured_at, runs=tuple(stats))
